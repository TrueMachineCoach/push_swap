char	*ft_skipper_blanks(char *str)
{
	while ((*str == '\n' || *str == '\r' || *str == '\t' ||
			*str == ' ' || *str == '\a' || *str == '\f'
				|| *str == '\v' || *str == '\b'))
		str++;
	return (str);
}
