int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	while (i <= nb && i >= 0 && i <= 46340)
	{
		if ((i * i) == nb)
			return (i);
		if ((i * i) > nb)
			return (0);
		i++;
	}
	return (0);
}
